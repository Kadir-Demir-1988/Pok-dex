function TypeColorClass(type) {
  switch (type) {
    case "grass":
      return "bgColorgrass";
    case "fire":
      return "bgColorfire";
    case "water":
      return "bgColorwater";
    case "bug":
      return "bgColorbug";
    case "normal":
      return "bgColornormal";
    case "poison":
      return "bgColorpoison";
    case "electric":
      return "bgColorlectric";
    case "ground":
      return "bgColorground";
    case "fairy":
      return "bgColorfairy";
    case "fighting":
      return "bgColorfighting";
    case "psychic":
      return "bgColorpsychic";
    case "rock":
      return "bgColorrock";
    case "ice":
      return "bgColorice";
    case "flying":
      return "bgColorflying";
    case "steel":
      return "bgColorsteel";
    case "ghost":
      return "bgColorpsychic";
    case "dragon":
      return "bgColordragon";
  }
}
